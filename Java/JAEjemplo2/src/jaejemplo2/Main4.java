public class Main4 {
    //Uso del ciclo mientras y ciclo repetir
    public static void main(String[] args) {
        
        //Uso del ciclo while o mientras
        int x=0;
        while(x<10){
            x++;
            System.out.println(x);
        }
        
        
        //Uso del ciclo do while o repetir hasta que.
        x=0;
        do{
            x++;
            System.out.println(x);
        }while(x<10);
    }
}
